    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement
        ("insert into aset_masuk (id_barang, kode_barang, nama_barang, tanggal_masuk, harga, jumlah_barang, total_harga) value (?,?,?,?,?,?,?)");
            stmt.setString(1, txtIdBarang.getText());
            stmt.setString(2, txtKodeBarang.getText());
            stmt.setString(3, txtNamaBarang.getText());
            stmt.setString(4, txtTanggalMasuk.getText());
            stmt.setString(5, txtHarga.getText());
            stmt.setString(6, txtJumlahBarang.getText());
            stmt.setString(7, txtTotalHarga.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            tampilData();
            bersih();
        }catch (SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

