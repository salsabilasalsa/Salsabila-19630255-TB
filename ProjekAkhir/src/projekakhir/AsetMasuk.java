    private void tampilData(){
        mod = new DefaultTableModel();
        mod.addColumn("Id Barang");
        mod.addColumn("Kode Barang");
        mod.addColumn("Nama Barang");
        mod.addColumn("Tanggal Masuk");
        mod.addColumn("Harga");
        mod.addColumn("Jumlah Barang");
        mod.addColumn("Total Harga");
        tabelAset.setModel(mod);
        Connection conn = koneksi.getConnection();
        try{
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from aset_masuk";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while(res.next()){
                mod.addRow(new Object[]{
                   res.getString("id_barang"),
                   res.getString("kode_barang"),
                   res.getString("nama_barang"),
                   res.getString("tanggal_masuk"),
                   res.getString("harga"),
                   res.getString("jumlah_barang"),
                   res.getString("total_harga"),
                });
            }
        }catch (SQLException e){
            System.out.println(e);
            
        }
            
    }

