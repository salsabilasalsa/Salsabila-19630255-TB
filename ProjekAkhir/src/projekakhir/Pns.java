    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        try{
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement
        ("insert into data_pns (nip, nama, tempat_lahir, jabatan, golongan, pendidikan_terakhir) value (?,?,?,?,?,?)");
            stmt.setString(1, txtNip.getText());
            stmt.setString(2, txtNama.getText());
            stmt.setString(3, txtTempatLahir.getText());
            stmt.setString(4, txtJabatan.getText());
            stmt.setString(5, txtGolongan.getText());
            stmt.setString(6, txtPendidikan.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan", "Pesan", JOptionPane.INFORMATION_MESSAGE);
            tampilData();
            bersih();
        }catch (SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

