    private void tampilData(){
        mod = new DefaultTableModel();
        mod.addColumn("NIP");
        mod.addColumn("Nama");
        mod.addColumn("Tempat Lahir");
        mod.addColumn("Jabatan");
        mod.addColumn("Golongan");
        mod.addColumn("Pendidikan Terakhir");
        tabelPns.setModel(mod);
        Connection conn = koneksi.getConnection();
        try{
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from data_pns";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while(res.next()){
                mod.addRow(new Object[]{
                   res.getString("nip"),
                   res.getString("nama"),
                   res.getString("tempat_lahir"),
                   res.getString("jabatan"),
                   res.getString("golongan"),
                   res.getString("pendidikan_terakhir"),
                });
            }
        }catch (SQLException e){
            System.out.println(e);
            
        }
            
    }

